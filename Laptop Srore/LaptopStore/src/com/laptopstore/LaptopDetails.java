package com.laptopstore;

public class LaptopDetails {
	int tot;
	String res;
	
	
	public LaptopDetails(int tot, String res) {
		super();
		this.tot = tot;
		this.res = res;
	}
	
	public int getTot() {
		return tot;
	}
	
	public void setTot(int tot) {
		this.tot = tot;
	}
	
	public String getRes() {
		return res;
	}
	
	public void setRes(String res) {
		this.res = res;
	}
	
}
